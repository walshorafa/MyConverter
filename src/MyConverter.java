public class MyConverter {

    public static double cmToMeter(double cm) {
        return cm / 100;
    }

    public static double meterToCm(double meter) {
        return meter * 100;
    }

    public static double meterToKm(double meter) {
        return meter / 1000;
    }

    public static double kmToMeter(double km) {
        return km * 1000;
    }





}
